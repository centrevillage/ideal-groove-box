#define DAC_TX_BUF_SIZE 256
#define DAC_RX_BUF_SIZE DAC_TX_BUF_SIZE
int32_t dac_tx_buffer[DAC_TX_BUF_SIZE];
int32_t dac_rx_buffer[DAC_RX_BUF_SIZE];

void MX_SAI_DMA_Init() {
  HAL_SAI_Transmit_DMA(&hsai_BlockA1, (uint8_t*)dac_tx_buffer, DAC_TX_BUF_SIZE);
  while (HAL_SAI_GetState(&hsai_BlockA1) != HAL_SAI_STATE_READY);
  HAL_SAI_Receive_DMA(&hsai_BlockB1, (uint8_t*)dac_rx_buffer, DAC_RX_BUF_SIZE);
  while (HAL_SAI_GetState(&hsai_BlockB1) != HAL_SAI_STATE_READY);
}

void dac_tx_buffer_init_by_saw() {
  const int32_t step = 0xFFFFFFL / DAC_TX_BUF_SIZE;
  for (uint32_t i=0; i<DAC_TX_BUF_SIZE; ++i) {
    dac_tx_buffer[i] = ((i * step));
  }
}

void dac_tx_buffer_init_by_zero() {
  for (uint32_t i=0; i<DAC_TX_BUF_SIZE; ++i) {
    dac_tx_buffer[i] = 0;
  }
}

void dac_rx_to_tx_buffer() {
  for (uint32_t i=0; i<DAC_RX_BUF_SIZE; ++i) {
    dac_tx_buffer[i] = dac_rx_buffer[i];
  }
}

